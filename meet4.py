#aritmatika

x = 12
y = 5

#penjumlahan

hasil = 12 + 5
print(x ,"+" ,y ,"=",hasil)

#pengurangan
hasil = 12 - 5
print(x ,"-" ,y ,"=",hasil)

#perkalian
hasil = 12 * 5
print(x ,"x" ,y ,"=",hasil)

#pembagian
hasil = 12 / 5
print(x ,"/" ,y ,"=",hasil)

#perpangkatan
hasil = 12 ** 5
print(x ,"^" ,y ,"=",hasil)

#modulus
hasil = 12 % 5
print(x ,"mod" ,y ,"=",hasil)

#floor devision
hasil = 12 // 5
print(x ,"floor" ,y ,"=",hasil)

